import { Router } from "express";
import { getAllUsers } from "../services/userService";

const router = Router();

/**
 * @swagger
 * /users:
 *   get:
 *     summary: Get all users
 *     tags: [Users]
 *     responses:
 *       200:
 *         description: List of all users
 *         content:
 *           application/json:
 *             schema:
 *               type: array
 *               items:
 *                 type: object
 *                 properties:
 *                   id:
 *                     type: integer
 *                   fullName:
 *                     type: string
 *                   email:
 *                     type: string
 */
router.get("/", async (req, res) => {
  const allUsers = await getAllUsers();
  res.json(allUsers);
});

export default router;